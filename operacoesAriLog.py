#Operações Aritméticas

#Soma
x1 = 14
x2 = 23
resultado = x1 + x2
print(resultado)

#Subtração
x1 = 26
x2 = 45
resultado = x1 - x2
print(resultado)

#Divisão
x1 = 40
x2 = 2
resultado = x1 / x2
print(resultado)

#Multiplicação
x1 = 5
x2 = 5
resultado = x1 * x2
print(resultado)

#Exponenciação
x1 = 20
x2 = 2
resultado = x1 ** x2
print(resultado)

#Resto da divisão
x1 = 47
x2 = 4
resultado = x1 % x2
print(resultado)

#Divisão Inteira 
x1 = 10
x2 = 2
resultado = x1 // x2
print(resultado)

#Operações Booleanas
# ==, !=, >, >=, < e <=

print(6 < 8)   # True
print(9 > 10)  # False
print(20 == 15)  # False
print(45 != 30)  # True
print(32 >= 32)  # True
print(26 <= 21)  # False

